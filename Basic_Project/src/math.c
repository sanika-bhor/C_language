#include<math.h>
int addtion(int num1, int num2){
    int result=num1+num2;
    return result;
}

int subtraction(int num1, int num2)
{
    int result = num1 - num2;
    return result;
}

int multiplication(int num1, int num2)
{
    int result = num1 * num2;
    return result;
}

int division(int num1, int num2)
{
    int result = num1 / num2;
    return result;
}

int modulus(int num1, int num2)
{
    int result = num1 % num2;
    return result;
}